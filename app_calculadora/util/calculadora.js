/*
    Esse módulo implementa as operações básicas
    de uma calculadora.
    Essas funções devem ser exportadas para serem usadas
    em outros arquivos fontes.
*/

export function somar(a, b) {
    return a + b;
}

export function subtrair(a, b) {
    return a - b;
}

export function multiplicar(a, b) {
    return a * b;
}

export function dividir(a, b) {
    return a / b;
}

export const PI = 3.14;

